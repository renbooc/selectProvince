def get_province_from_local(district_name):
    """从本地数据中查询省份"""
    district_name = district_name.strip()

    # 先检查是否是省份名称（精确匹配）
    if district_name in LOCAL_AREA_DATA:
        province_data = LOCAL_AREA_DATA[district_name]
        return {
            "province": district_name,
            "level": "省级",
            "code": "",
            "intro": f"{district_name}位于中国{get_direction(district_name)}，是中国{get_direction_desc(district_name)}的重要省份。",
        }

    # 遍历所有省份
    for province, province_data in LOCAL_AREA_DATA.items():
        cities = province_data.get("cities", {})

        # 检查是否是城市名称（精确匹配或模糊匹配）
        for city_name, city_data in cities.items():
            # 精确匹配
            if district_name == city_name:
                return {
                    "province": province,
                    "city": district_name,
                    "level": "市级",
                    "code": "",
                    "intro": city_data.get(
                        "intro", f"{district_name}是{province}的重要城市。"
                    ),
                }

            # 模糊匹配：用户输入"汕头"匹配"汕头市"
            if district_name in city_name or city_name in district_name:
                return {
                    "province": province,
                    "city": city_name,
                    "level": "市级",
                    "code": "",
                    "intro": city_data.get(
                        "intro", f"{city_name}是{province}的重要城市。"
                    ),
                }

            # 检查是否是区县名称
            districts = city_data.get("districts", [])
            for district in districts:
                # 精确匹配
                if district_name == district:
                    return {
                        "province": province,
                        "city": city_name,
                        "district": district_name,
                        "level": "县级",
                        "code": "",
                        "intro": f"{district_name}位于{city_name}，隶属于{province}。",
                    }

                # 模糊匹配：用户输入"祁东"匹配"祁东县"
                if district_name in district or district in district_name:
                    return {
                        "province": province,
                        "city": city_name,
                        "district": district,
                        "level": "县级",
                        "code": "",
                        "intro": f"{district}位于{city_name}，隶属于{province}。",
                    }

    return None


